package com.demo.calculator;

public class Adder {

	
	public int sum(int a, int c) {

	int sum;
	sum = a + c;
	return sum;
}

	public int difference(int a, int c) {

	int difference;
	difference = a - c;
	return difference;

}

	public int product(int a, int c){

	int product;
	product = a * c;
	return product;
}

	public int quo(int a, int c){

	int quo;
	quo = a / c;
	return quo;
}


}




